import json

from datasets import Dataset
from langchain_openai import ChatOpenAI, OpenAIEmbeddings
from ragas import evaluate
from ragas.embeddings import LangchainEmbeddingsWrapper
from ragas.llms import LangchainLLMWrapper
from ragas.metrics import answer_relevancy, context_precision, faithfulness

# =========================
# CONFIG
# =========================

MODE = "no_rag"  # change to "no_rag" if needed

RESULT_PATH = f"/Users/novay/Applications/Project/Enterwind/lexindo/python/lexindo-llm/07_experiments/{MODE}_results.json"
GROUND_TRUTH_PATH = "/Users/novay/Applications/Project/Enterwind/lexindo/python/lexindo-llm/05_evaluation_set/evaluation_ground_truth.json"

# =========================
# LOAD DATA
# =========================

with open(RESULT_PATH, "r", encoding="utf-8") as f:
    results = json.load(f)

with open(GROUND_TRUTH_PATH, "r", encoding="utf-8") as f:
    ground_truth = json.load(f)

gt_map = {g["question_id"]: g for g in ground_truth}

# =========================
# BUILD RAGAS DATASET
# =========================

data = []

for item in results:
    qid = item["question_id"]

    data.append(
        {
            "question": item["question"],
            "answer": item["model_answer"],
            "contexts": [item["retrieved_context"]]
            if item["retrieved_context"]
            else [],
            "ground_truth": gt_map[qid]["ground_truth_context"],
        }
    )

dataset = Dataset.from_list(data)

# =========================
# SETUP LLM + EMBEDDING
# =========================

# Gunakan OpenAI untuk evaluator
llm = LangchainLLMWrapper(ChatOpenAI(model="gpt-4o-mini", temperature=0))

embeddings = LangchainEmbeddingsWrapper(
    OpenAIEmbeddings(model="text-embedding-3-small")
)

# =========================
# RUN EVALUATION
# =========================

result = evaluate(
    dataset,
    metrics=[faithfulness, answer_relevancy, context_precision],
    llm=llm,
    embeddings=embeddings,
)

print("\n===== RAGAS RESULTS =====")
print(result)

